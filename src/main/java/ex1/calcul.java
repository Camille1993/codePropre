package ex1;

/**
 * Classe qui realise des operations arythmetiques
 * @author DIGINAMIC
 */

public class calcul {

	/** Methode qui retourne l'addition des parametres a et b 
	 * @param a 
	 * @param b 
	 * @return int 
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
